let soma = (valor1, valor2) => {
    return `Resultado: ${valor1 + valor2}`
}

let subtrai = (valor1, valor2) => {
    return `Resultado: ${valor1 - valor2}`
}

let multiplica = (valor1, valor2) => {
    return `Resultado: ${valor1 * valor2}`
}

let divide = (valor1, valor2) => {
    return `Resultado: ${valor1 / valor2}`
}

export { soma, subtrai, multiplica, divide};

